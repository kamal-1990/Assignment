package day19;

public class Multidimensional {

	public static void main(String[] args) {
		
       

       int[][] arr=new int[][] {{3,4,5},{6,4,7},{0,1,9}};
       for (int i = 0; i < arr.length; i++) {
    	   for (int j = 0; j < arr.length; j++) {
			System.out.print(arr[i][j]+" ");
		
		}
		
	System.out.println();
       }
       
       
       
       
	}

}
